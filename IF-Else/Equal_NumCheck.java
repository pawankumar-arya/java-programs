public class Equal_NumCheck {
  public static void main(String[] args) {
    int num1 = 25;
    int num2 = 25;

    if (num1 == num2) {
      System.out.println("Both numbers are equal: " + num1 + " and " + num2);
    } else {
      System.out.println("Numbers are not equal: " + num1 + " and " + num2);
    }
  }
}
