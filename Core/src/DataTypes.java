 class DataTypes {
    public static void main(String[] args) {
        int num = 10 ;
        char ch = 'c' ;
        String str = "hello" ;
        float ft = 2.22f ;
        double db = 2.25 ;
        boolean bl = true ;
        System.out.println(num);
        System.out.println(ch);
        System.out.println(str);
        System.out.println(ft);
        System.out.println(db);
        System.out.println(bl);
    }
}
