public class DataTypes {
  public static void main(String[] args) {
    // Data Types declaration
    int myInt = 42;  // Integer data type
    double myDouble = 3.14;  // Double data type
    char myChar = 'A';  // Character data type
    boolean myBoolean = true;  // Boolean data type
    String myString = "Hello, Data Types!";  // String data type
    float myFloat = 5.67f;  // Float data type
    System.out.println("Integer: " + myInt);
    System.out.println("Double: " + myDouble);
    System.out.println("Character: " + myChar);
    System.out.println("Boolean: " + myBoolean);
    System.out.println("String: " + myString);
    System.out.println("Float: " + myFloat);
  }
}
